void _sys_set_errno (unsigned long rc)
{
  if (rc >= sizeof (errno_tab))
    errno = EINVAL;
  else
    errno = errno_tab[rc];
  LIBCLOG_MSG2("Set errno %d for OS/2 error %ld\n", errno, rc);
}

int __libc_native2errno (unsigned long rc)
{
  if (rc >= sizeof (errno_tab))
    return EINVAL;
  return errno_tab[rc];
}

